# Mad Libs Game in Ruby

# Prompt the user to enter words for the Mad Lib
print "Enter a noun: "
sasuke1 = gets.chomp

print "Enter another noun: "
sasuke2 = gets.chomp

# Output the completed Mad Lib
puts "sasuke: I'm the only one who could destroy that #{sasuke1}."
puts "sakura: What? Who? You mean the sensei?"
puts "sasuke: that #{sasuke2} I was crying."
